#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	j;

	j = 0;
	while (argc-- > 1)
	{
		j = 0;
		while (argv[argc][j])
		{
			write(1, &argv[argc][j], 1);
			j++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
